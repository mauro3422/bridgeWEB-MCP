import fs from "node:fs";
import { createDefaultToolRegistry } from "../dist/tool-registry.js";
import { SERVER_NAME, SERVER_VERSION } from "../dist/config.js";

const CHECK_MODE = process.argv.includes("--check");
const OUT_PATH = "TOOLS.md";

const registry = createDefaultToolRegistry();

const groupRules = [
  ["Core / navegacion", ["system_info", "list_dir", "read_text_file", "read_file_lines", "read_many_files", "list_files_smart", "search_files"]],
  ["Escritura segura", ["write_text_file", "apply_patch", "edit_lines"]],
  ["Guias reutilizables", ["workflow_guide_recommend", "workflow_guide_load", "workflow_guide_create"]],
  ["Imagenes", ["image_asset_save", "image_character_views_prepare"]],
  ["Ejecucion / terminal", ["run_command", "terminal_start", "terminal_write", "terminal_read", "terminal_stop", "terminal_list"]],
  ["Blender", ["blender_status", "blender_open", "blender_scene_info", "blender_viewport_screenshot", "blender_execute_code", "blender_batch_script", "blender_store_reference_image", "blender_setup_character_references", "blender_character_loop_status"]],
  ["Git", ["git_status", "git_set_remote", "git_commit_all", "git_push_current_branch"]],
  ["Bridge ops", ["tunnel_health", "bridge_self_check", "bridge_verify_all", "bridge_request_restart", "bridge_restart_status"]],
  ["Metricas / visualizaciones", ["bridge_metrics_status", "bridge_metrics_summary", "bridge_metrics_recent", "bridge_visualization_catalog", "bridge_visualize_metrics"]],
  ["Inteligencia de codigo", ["analyze_code", "impact_analysis", "find_duplicate_symbols", "import_graph", "dependency_graph", "call_graph", "find_dead_code"]],
];

function schemaBlock(schema) {
  return JSON.stringify(schema ?? {}, null, 2);
}

function requiredList(schema) {
  return Array.isArray(schema?.required) ? schema.required.join(", ") : "ninguno";
}

function propsList(schema) {
  const props = schema?.properties ?? {};
  const rows = Object.entries(props).map(([name, prop]) => {
    const type = Array.isArray(prop?.type) ? prop.type.join("|") : prop?.type ?? "unknown";
    const def = Object.prototype.hasOwnProperty.call(prop ?? {}, "default") ? ` default=${JSON.stringify(prop.default)}` : "";
    const en = Array.isArray(prop?.enum) ? ` enum=${prop.enum.join("|")}` : "";
    const range = [prop?.minimum !== undefined ? `min=${prop.minimum}` : null, prop?.maximum !== undefined ? `max=${prop.maximum}` : null].filter(Boolean).join(" ");
    return `- \`${name}\`: ${type}${en}${def}${range ? ` ${range}` : ""}`;
  });
  return rows.join("\n") || "- sin parametros";
}

function normalizeNewlines(text) {
  return text.replace(/\r\n/g, "\n");
}

function buildToolsDoc() {
  const toolsByName = new Map(registry.tools.map((tool) => [tool.name, tool]));
  const seen = new Set();
  let out = "";
  out += "# bridge-mcp TOOLS\n\n";
  out += "Generated from the modular registry definitions/schemas. Do not edit this file by hand; regenerate it with `npm run docs:tools` after schema changes.\n\n";
  out += `Server: \`${SERVER_NAME} v${SERVER_VERSION}\`\n\n`;
  out += `Total tools: ${registry.tools.length}\n\n`;
  out += "Modules loaded:\n\n";
  out += registry.modules.map((moduleName) => `- \`${moduleName}\``).join("\n") + "\n\n";
  out += "Source of truth:\n\n";
  out += "```txt\n";
  out += "src/tool-registry.ts\n";
  out += "src/tools/*.ts\n";
  out += "BridgeToolSchema.inputSchema definitions\n";
  out += "```\n\n";
  out += "## Uso recomendado\n\n";
  out += "Preferir tools explicitas antes que `run_command`. Usar `run_command` como fallback cuando no exista una tool especifica o para validaciones de shell.\n\n";
  out += "Para cambios de codigo: inspeccionar con lectura/busqueda/analisis, editar con `apply_patch` o `edit_lines`, correr `bridge_verify_all`, y recien despues commitear/pushear.\n\n";
  out += "## Grupos\n\n";

  for (const [group, names] of groupRules) {
    out += `### ${group}\n\n`;
    for (const name of names) {
      const tool = toolsByName.get(name);
      if (!tool) continue;
      seen.add(name);
      out += `#### \`${tool.name}\`\n\n`;
      out += `${tool.description}\n\n`;
      out += `Required: ${requiredList(tool.inputSchema)}\n\n`;
      out += `Parametros:\n\n${propsList(tool.inputSchema)}\n\n`;
      out += "<details>\n<summary>Input schema</summary>\n\n";
      out += `\`\`\`json\n${schemaBlock(tool.inputSchema)}\n\`\`\`\n\n`;
      out += "</details>\n\n";
    }
  }

  const leftovers = registry.tools.filter((tool) => !seen.has(tool.name));
  if (leftovers.length > 0) {
    out += "### Otras tools\n\n";
    for (const tool of leftovers) {
      out += `#### \`${tool.name}\`\n\n${tool.description}\n\n`;
      out += `\`\`\`json\n${schemaBlock(tool.inputSchema)}\n\`\`\`\n\n`;
    }
  }

  out += "## Notas de seguridad\n\n";
  out += "- No leer ni imprimir secretos, tokens ni archivos `.env`.\n";
  out += "- No commitear `node_modules`, `dist`, logs, bases SQLite, sandbox ni binarios del tunnel-client.\n";
  out += "- Para restart, usar `bridge_request_restart`; no matar procesos directamente desde el MCP activo.\n";
  out += "- `run_command` es fallback, no el camino principal para editar o diagnosticar cuando hay tools especificas.\n";
  return out;
}

const generated = buildToolsDoc();

if (CHECK_MODE) {
  const current = fs.existsSync(OUT_PATH) ? fs.readFileSync(OUT_PATH, "utf8") : "";
  if (normalizeNewlines(current) !== normalizeNewlines(generated)) {
    console.error(`${OUT_PATH} is out of date. Run: npm run docs:tools`);
    process.exit(1);
  }
  console.log(`${OUT_PATH} is up to date with ${registry.tools.length} registry tools`);
} else {
  fs.writeFileSync(OUT_PATH, generated, "utf8");
  console.log(`wrote ${OUT_PATH} with ${registry.tools.length} tools`);
}
